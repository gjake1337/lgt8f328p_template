/**********************************************************************************
*** 						  	EXPORTED FUNCTIONS								*** 													
**********************************************************************************/

// ----------------------------------------------------------------------
// public: AC0 module initialization
// ----------------------------------------------------------------------
void ac0Init(void)
{
	ac0SetPosedge(AC0_PSEL_AC0P);
	ac0SetNegedge(AC0_NSEL_ACXN);

	C0SR |= (1 << AC0_C0FS_OFF) | AC0_ISS_POS;
	C0XR |= (1 << AC0_C0FS_OFF) | (1 << C0HSYE);	
}

// ----------------------------------------------------------------------
// public: AC1 module initialization
// ----------------------------------------------------------------------
void ac1Init(void)
{
	ac1SetPosedge(AC1_PSEL_AC1P);
	ac1SetNegedge(AC1_NSEL_AC1N);

	C1SR |= (1 << AC1_C1FS_OFF) | AC1_ISS_POS;
	C1XR |= (1 << AC1_C1FS_OFF) | (1 << C1HSYE);	
}



